var TxtRotate = function(el, toRotate, period) {
  this.toRotate = toRotate;
  this.el = el;
  this.loopNum = 0;
  this.period = parseInt(period, 10) || 1000;
  this.txt = '';
  this.tick();
  this.isDeleting = false;
};

var TxtRotate = function(element, toRotate, period) {
  this.toRotate = toRotate;
  this.element = element;
  this.loopNum = 0;
  this.period = parseInt(period, 10) || 1000;
  this.txt = '';
  this.isDeleting = false;
  this.tick();
};

TxtRotate.prototype.tick = function() {
  var i = this.loopNum % this.toRotate.length;
  var fullText = this.toRotate[i];

  // Determine the indefinite article ('a' or 'an') based on the next word
  var nextWord = fullText.split(' ')[0]; // Get the first word of the next text
  var indefiniteArticle = /^[aeiou]/i.test(nextWord) ? 'an' : 'a';

  if (this.isDeleting) {
    this.txt = fullText.substring(0, this.txt.length - 1);
  } else {
    this.txt = fullText.substring(0, this.txt.length + 1);
  }

  // Update the displayed text including the indefinite article
  this.element.innerHTML = '<span class="wrap">' + indefiniteArticle + ' ' + this.txt + '</span>';

  var that = this;
  var delta = 100; // Set the typing speed

  if (this.isDeleting) {
    delta /= 2; // Speed up deleting
  }

  if (!this.isDeleting && this.txt === fullText) {
    delta = this.period;
    this.isDeleting = true;
  } else if (this.isDeleting && this.txt === '') {
    this.isDeleting = false;
    this.loopNum++;
    delta = 100; // Pause before typing next sequence
  }

  setTimeout(function() {
    that.tick();
  }, delta);
};

// Initialization on window load
window.onload = function() {
  var elements = document.getElementsByClassName('txt-rotate');
  for (var i = 0; i < elements.length; i++) {
    var toRotate = elements[i].getAttribute('data-rotate');
    var period = elements[i].getAttribute('data-period');
    if (toRotate) {
      new TxtRotate(elements[i], JSON.parse(toRotate), period);
    }
  }

  // Inject CSS for text animation
  var css = document.createElement("style");
  css.innerHTML = ".txt-rotate > .wrap { border-right: 0.08em solid #666 }";
  document.body.appendChild(css);
};